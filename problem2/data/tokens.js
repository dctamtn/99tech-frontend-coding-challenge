// Generate available tokens from tokenPrices with automatic icon detection
export const availableTokens = tokenPrices.map(priceData => {
  const symbol = priceData.currency;
  
  // Find the corresponding icon file
  let iconPath = null;
  
  // First try exact match
  if (availableTokenIcons.includes(`${symbol}.svg`)) {
    iconPath = `tokens/${symbol}.svg`;
  }
  // Try with different extensions
  else if (availableTokenIcons.includes(`${symbol}.png`)) {
    iconPath = `tokens/${symbol}.png`;
  }
  // Try lowercase
  else if (availableTokenIcons.includes(`${symbol.toLowerCase()}.svg`)) {
    iconPath = `tokens/${symbol.toLowerCase()}.svg`;
  }
  // Try uppercase
  else if (availableTokenIcons.includes(`${symbol.toUpperCase()}.svg`)) {
    iconPath = `tokens/${symbol.toUpperCase()}.svg`;
  }
  // If no icon found, use a default token icon
  else {
    iconPath = 'tokens/Token.svg';
  }
  
  return {
    symbol: symbol,
    name: symbol, // You can expand this with a proper name mapping if needed
    icon: iconPath
  };
});

// Utility functions
export const getTokenPrice = (symbol) => {
  const priceData = tokenPrices.find(p => p.currency === symbol);
  return priceData ? priceData.price : null;
};

export const formatNumber = (num, decimals = 6) => {
  if (num === 0) return '0';
  if (num < 0.000001) return '< 0.000001';
  return Number(num).toLocaleString('en-US', {
    minimumFractionDigits: 0,
    maximumFractionDigits: decimals
  });
};

export const calculateExchangeRate = (fromToken, toToken) => {
  const fromPrice = getTokenPrice(fromToken.symbol);
  const toPrice = getTokenPrice(toToken.symbol);
  
  if (!fromPrice || !toPrice) return null;
  
  return fromPrice/toPrice;
};

export const calculateToAmount = (fromAmount, fromToken, toToken) => {
  const rate = calculateExchangeRate(fromToken, toToken);
  if (!rate) return 0;
  
  return fromAmount * rate;
};

export const calculatePriceImpact = (fromAmount, fromToken, toToken) => {
  const fromPrice = getTokenPrice(fromToken.symbol);
  if (!fromPrice) return 0;
  
  const usdValue = fromAmount * fromPrice;
  if (usdValue < 1000) return 0.01;
  if (usdValue < 10000) return 0.05;
  if (usdValue < 100000) return 0.15;
  return 0.5;
};

